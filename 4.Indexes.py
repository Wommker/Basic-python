"""

Strings or text variables in python are
They can work in different ways
Here are some ways to work the same
string

"""

var = "Hello world, all is well"


#print the variable

print(var)

#Print a specific character of the variable

print(var[0])

#Print a specific character of the variable from right to left

print(var[-3])

#Prints two specific characters of the variable

print(var[0],var[-3])

#Print the characters from 0 to 9

print(var[0:10])

#Print characters from 0 to 10 every 2 characters

print(var[0:10:2])

#Print the entire variable backwards

print(var[::-1])