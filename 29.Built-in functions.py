#Some functions built into python

#Convert from string to number

print(int("10"))

#Convert from number to string

print(str(10))

#Convert from number to binary

print(bin(10))

#Convert from number to hexadecimal

print(hex(10))

#Convert from binary to number

print(int("0b1010",2))

#Convert from hexadecimal to number

print(int("0xa",16))

#convert to absolute value

print(abs(-8))

#Round down to the lowest number

print(round(8,9))

#Returns the size of the string

print(len("Ten"))